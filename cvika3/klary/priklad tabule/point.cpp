#include "point.h"

Point::Point()
    :Point(0,0)
{

}
/*---------------------------------------------------*/
Point::Point(int x, int y):
    m_x(x), m_y(y)
{
}
/*---------------------------------------------------*/
int &Point::x()
{
    return m_x;
}
/*---------------------------------------------------*/
int &Point::y()
{
    return m_y;
}
/*---------------------------------------------------*/
const int &Point::x() const
{
    return m_x;
}
/*---------------------------------------------------*/
const int &Point::y() const
{
    return m_y;
}
